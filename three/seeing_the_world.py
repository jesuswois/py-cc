places = ["chignahuapan","necaxa","cancún","polo norte","francia"]  

print("Original list...")
print(places)

print("\nSorted list (sorted())")
print(sorted(places))

print("\nOriginal list...")
print(places)

places.reverse()
print("\nReversed list")
print(places)

places.reverse()
print("\nReversed list")
print(places)

places.sort()
print("\nSorted list... (.sort)")
print(places)

places.sort(reverse=True)
print("\nSorted list but reverse... (.sort(reverse=True))")
print(places)